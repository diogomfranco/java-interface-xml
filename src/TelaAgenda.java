    private void btnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarActionPerformed
        
        btnGravar.setEnabled(false);
        btnCancelar.setEnabled(false);
        jTextField1.setEditable(false);
        jTextField2.setEditable(false);
        jTextField3.setEditable(false);
        jTextField4.setEditable(false);
        jTextArea1.setEditable(false);
        jComboBox1.setEnabled(false);
        jTextField5.setEditable(false);
        jTextField6.setEditable(false);
        jCheckBox1.setEnabled(false);
        btnEditar.setEnabled(true);
        btnInserir.setEnabled(true);
        btnExcluir.setEnabled(true);
        
       Contato novoCont = new Contato(jTextField1.getText(), 
               jTextField2.getText(),
               jTextField3.getText(),
               jTextField4.getText(),
               jTextArea1.getText(), (String) jComboBox1.getSelectedItem(),
               jTextField5.getText(),
               jTextField6.getText(),
               jCheckBox1.isSelected());
        
        UUID uuid = UUID.randomUUID();

        String codigoUsuario = uuid.toString();
        listaContatos.add(novoCont);
        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
        Object[] adicionaEsseCaralho = {
            jTextField1.getText(),
            jComboBox1.getSelectedItem(),
            jCheckBox1.isSelected(),
            codigoUsuario,
        };
        model.addRow(adicionaEsseCaralho);
        
    }//GEN-LAST:event_btnGravarActionPerformed

